# Slicing the strings
# syntax: stringname[start:stop:stepsize]
str="Core python"
print(str[::])
print(str[:9:1])
print(str[:9:2])
print(str[2:4:1])
print(str[::2])
print(str[2::])
print(str[:4:])
print(str[-4:-1])

# Repeating the string
print(str*2)
print(str[5:7]*2)

# Concatenation of strings
s1='Core'
s2='Python'
s3=s1+s2
print(s3)

