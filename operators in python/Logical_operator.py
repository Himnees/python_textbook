# Logical operaor
x=100
y=200
# And operator. If x is False, it returns x, otherwise it returns y.
print(x and y) # will display 200
# Or operator. If x is False, it returns y, otherwise it returns x.
print(x or y) # will display 100
# Not operator. If x is False, it returns True, otherwise True.
print(not x) # will display False
a=1; b=2; c=3
if(a<b and b<c):print('Yes')
else: print('No')
if(a>b or b<c):print('Yes')
else: print('No')