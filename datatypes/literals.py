# Literals in Python
# 1) Numeric Literals
a,b = 450,-15 # Interger literals
c,d = 3.142,1.2e4 # Float literals
e = 0x5A1C # Hexadecimal literals
f = 0o0557  # Octal literals
g = 0b110101  # Binary literals
h = 18+3J   # Complex Literals
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)

# 2)Boolean Literals
#Boolean literals are the True or False values stored stored into a bool type variable
b1=10
b2=20
print(b1>b2)
print(b1<b2)

