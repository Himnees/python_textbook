# Program 1: A python program to create teacher class and store it into teacher.py module.

class Teacher:
    def setid(self,id):
        self.id=id
    def getid(self):
        return self.id
    
    def setname(self,name):
        self.name=name
    def getname(self):
        return self.name
    
    def setAdress(self,address):
        self.address=address
    def getAddress(self):
        return self.address
    
    def setSalary(self,salary):
        self.salary=salary
    def getSalary(self):
        return self.salary